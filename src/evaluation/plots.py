"""
src/evaluation/plots.py
------------------------------------------------------------------------------
Plotting functions for training curves and ensemble metric comparison.

This module produces two categories of plots:

1. Training curves (generated during/after training each model):
   - Loss vs epoch (train and validation on the same plot)
   - Accuracy vs epoch (train and validation on the same plot)
   These help you diagnose training behaviour:
     - Are train and val curves converging together? → Good generalisation.
     - Is train loss decreasing but val loss increasing? → Overfitting.
     - Are both curves flat from early on? → Learning rate too low, or
       the frozen backbone is too restrictive.

2. Ensemble comparison bar chart (generated by scripts/ensemble_eval.py):
   - Groups bars by metric (Accuracy, Precision, Recall, F1, F2)
   - Each bar represents one fusion method
   - Lets you see at a glance which method performs best overall and
     which metric each method is strongest/weakest on

All figures are saved to PNG files rather than displayed interactively,
so that training scripts can run without requiring a display (e.g. on a
server or in a batch job). Set show=True if you want to see them live.
"""

import matplotlib.pyplot as plt
import matplotlib.ticker as mticker
import numpy as np
import pandas as pd
from pathlib import Path

from src.utils.logger import get_logger

logger = get_logger(__name__)


# ==============================================================================
# Training curves
# ==============================================================================

def plot_training_curves(
    history: dict,
    model_name: str,
    save_dir: Path,
    show: bool = False
) -> None:
    """
    Plot and save loss and accuracy curves for a completed training run.

    Produces two separate figures:
      - {model_name}_loss_curve.png
      - {model_name}_accuracy_curve.png

    Parameters
    ----------
    history : dict
        The dictionary returned by base_model.train_model(). Must contain:
          "train_losses", "val_losses", "train_accs", "val_accs"
        Each is a list of floats, one per epoch.
    model_name : str
        Used in the plot title and filename, e.g. "vgg16".
    save_dir : Path
        Directory to save the PNG files into.
    show : bool
        If True, display the figure interactively. Default False.
    """
    save_dir.mkdir(parents=True, exist_ok=True)

    # The x-axis is the epoch number, starting from 1 (not 0).
    # range(1, n+1) generates [1, 2, 3, ..., n].
    num_epochs = len(history["train_losses"])
    epochs     = range(1, num_epochs + 1)

    # ------------------------------------------------------------------
    # Loss curve
    # ------------------------------------------------------------------
    fig, ax = plt.subplots(figsize=(9, 5))

    ax.plot(epochs, history["train_losses"], label="Train Loss",
            color="steelblue", linewidth=2, marker="o", markersize=3)
    ax.plot(epochs, history["val_losses"],   label="Validation Loss",
            color="tomato",    linewidth=2, marker="o", markersize=3,
            linestyle="--")

    ax.set_xlabel("Epoch", fontsize=12)
    ax.set_ylabel("Loss",  fontsize=12)
    ax.set_title(f"{model_name.upper()} — Loss vs Epoch",
                 fontsize=14, fontweight="bold")
    ax.legend(fontsize=11)
    ax.grid(True, alpha=0.3)   # Light grid lines — easier to read values

    # Force the x-axis to show only integer tick marks (epoch numbers).
    ax.xaxis.set_major_locator(mticker.MaxNLocator(integer=True))

    plt.tight_layout()

    loss_path = save_dir / f"{model_name}_loss_curve.png"
    fig.savefig(loss_path, dpi=150, bbox_inches="tight")
    logger.info(f"Loss curve saved: {loss_path}")

    if show:
        plt.show()
    plt.close(fig)

    # ------------------------------------------------------------------
    # Accuracy curve
    # ------------------------------------------------------------------
    fig, ax = plt.subplots(figsize=(9, 5))

    ax.plot(epochs, history["train_accs"], label="Train Accuracy",
            color="steelblue", linewidth=2, marker="o", markersize=3)
    ax.plot(epochs, history["val_accs"],   label="Validation Accuracy",
            color="tomato",    linewidth=2, marker="o", markersize=3,
            linestyle="--")

    ax.set_xlabel("Epoch",    fontsize=12)
    ax.set_ylabel("Accuracy", fontsize=12)
    ax.set_title(f"{model_name.upper()} — Accuracy vs Epoch",
                 fontsize=14, fontweight="bold")
    ax.legend(fontsize=11)
    ax.grid(True, alpha=0.3)

    # Set y-axis range to [0, 1] so all models are on the same scale
    # and curves from different models can be visually compared.
    ax.set_ylim(0.0, 1.05)
    ax.xaxis.set_major_locator(mticker.MaxNLocator(integer=True))

    plt.tight_layout()

    acc_path = save_dir / f"{model_name}_accuracy_curve.png"
    fig.savefig(acc_path, dpi=150, bbox_inches="tight")
    logger.info(f"Accuracy curve saved: {acc_path}")

    if show:
        plt.show()
    plt.close(fig)


def plot_combined_training_curves(
    histories: dict,
    save_dir: Path,
    show: bool = False
) -> None:
    """
    Plot validation accuracy curves for all models on a single figure.

    This makes it easy to compare how each model trained relative to the
    others — which converged faster, which reached higher accuracy, etc.

    Parameters
    ----------
    histories : dict
        Keys are model names (e.g. "vgg16"), values are history dicts
        as returned by base_model.train_model().
    save_dir : Path
        Directory to save the PNG file into.
    show : bool
        Whether to display interactively.
    """
    save_dir.mkdir(parents=True, exist_ok=True)

    # Assign a distinct colour to each model for clarity.
    colours = ["steelblue", "tomato", "seagreen", "darkorange", "purple"]

    fig, ax = plt.subplots(figsize=(10, 6))

    for (model_name, history), colour in zip(histories.items(), colours):
        num_epochs = len(history["val_accs"])
        epochs     = range(1, num_epochs + 1)
        ax.plot(
            epochs, history["val_accs"],
            label=model_name.upper(),
            color=colour,
            linewidth=2,
            marker="o",
            markersize=3
        )

    ax.set_xlabel("Epoch",               fontsize=12)
    ax.set_ylabel("Validation Accuracy", fontsize=12)
    ax.set_title("Validation Accuracy Comparison — All Models",
                 fontsize=14, fontweight="bold")
    ax.legend(fontsize=11)
    ax.grid(True, alpha=0.3)
    ax.set_ylim(0.0, 1.05)
    ax.xaxis.set_major_locator(mticker.MaxNLocator(integer=True))

    plt.tight_layout()

    save_path = save_dir / "all_models_val_accuracy.png"
    fig.savefig(save_path, dpi=150, bbox_inches="tight")
    logger.info(f"Combined accuracy curve saved: {save_path}")

    if show:
        plt.show()
    plt.close(fig)


