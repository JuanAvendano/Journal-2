def plot_metric_comparison(
    comparison_rows: list,
    save_dir: Path,
    show: bool = False
) -> None:
    """
    Plot a grouped bar chart comparing all ensemble methods across metrics.

    Each group of bars corresponds to one metric (Accuracy, Precision, etc.).
    Within each group, each bar represents one fusion method.
    This layout makes it easy to see:
      - Which method is best for each metric.
      - Which metric shows the most variation across methods.

    Parameters
    ----------
    comparison_rows : list of dict
        As returned by metrics.build_comparison_table(). Each dict has keys:
        "Method", "Accuracy", "Precision", "Recall", "F1", "F2".
    save_dir : Path
        Directory to save the PNG into.
    show : bool
        Whether to display interactively.
    """
    save_dir.mkdir(parents=True, exist_ok=True)

    # Convert to DataFrame for easy plotting.
    df = pd.DataFrame(comparison_rows)
    df = df.set_index("Method")

    metrics_to_plot = ["Accuracy", "Precision", "Recall", "F1", "F2"]

    # ------------------------------------------------------------------
    # Grouped bar chart
    # ------------------------------------------------------------------
    fig, ax = plt.subplots(figsize=(11, 6))

    # DataFrame.plot(kind="bar") with multiple columns produces grouped bars.
    # Each column is one metric, each row (index) is one method.
    # We transpose so that metrics are the groups and methods are the bars.
    df[metrics_to_plot].T.plot(
        kind="bar",
        ax=ax,
        edgecolor="white",
        width=0.7
    )

    ax.set_xlabel("Metric",  fontsize=12)
    ax.set_ylabel("Score",   fontsize=12)
    ax.set_title("Ensemble Method Comparison — All Metrics",
                 fontsize=14, fontweight="bold")

    # Set y-axis from 0 to 1 — all metrics are in this range.
    ax.set_ylim(0.0, 1.05)

    # Horizontal grid lines make it easier to read exact values.
    ax.yaxis.grid(True, alpha=0.4)
    ax.set_axisbelow(True)    # Draw grid lines behind the bars

    # Rotate x-axis tick labels (metric names) so they don't overlap.
    plt.xticks(rotation=0, fontsize=11)
    ax.legend(title="Method", fontsize=10, title_fontsize=10)

    plt.tight_layout()

    save_path = save_dir / "ensemble_metric_comparison.png"
    fig.savefig(save_path, dpi=150, bbox_inches="tight")
    logger.info(f"Metric comparison chart saved: {save_path}")

    if show:
        plt.show()
    plt.close(fig)


def plot_per_class_comparison(
    method_metrics: dict,
    class_names: list,
    save_dir: Path,
    show: bool = False
) -> None:
    """
    Plot per-class F1 scores for all ensemble methods as a grouped bar chart.

    This reveals which damage types each method handles well or poorly.
    For example, one method might be excellent at detecting cracks but
    struggle with efflorescence — this plot makes that visible.

    Parameters
    ----------
    method_metrics : dict
        Keys are method names, values are metric dicts from calculate_metrics().
    class_names : list of str
        Class names in label-index order.
    save_dir : Path
        Directory to save into.
    show : bool
        Whether to display interactively.
    """
    save_dir.mkdir(parents=True, exist_ok=True)

    methods = list(method_metrics.keys())
    x       = np.arange(len(class_names))   # One position per class on x-axis
    width   = 0.8 / len(methods)            # Bar width scales with number of methods

    fig, ax = plt.subplots(figsize=(10, 6))

    colours = ["steelblue", "tomato", "seagreen", "darkorange", "purple"]

    for i, (method, colour) in enumerate(zip(methods, colours)):
        # Extract F1 score for each class from the nested metrics dict.
        f1_scores = [
            method_metrics[method]["per_class"][cls]["f1"]
            for cls in class_names
        ]
        # Offset each method's bars so they appear side by side.
        # i * width shifts each method's bars to the right.
        offsets = x - (len(methods) - 1) * width / 2 + i * width
        ax.bar(
            offsets, f1_scores,
            width=width,
            label=method.replace("_", " ").title(),
            color=colour,
            edgecolor="white"
        )

    ax.set_xlabel("Damage Class", fontsize=12)
    ax.set_ylabel("F1 Score",     fontsize=12)
    ax.set_title("Per-Class F1 Score — Ensemble Method Comparison",
                 fontsize=14, fontweight="bold")
    ax.set_xticks(x)
    ax.set_xticklabels([c.title() for c in class_names], fontsize=11)
    ax.set_ylim(0.0, 1.05)
    ax.yaxis.grid(True, alpha=0.4)
    ax.set_axisbelow(True)
    ax.legend(title="Method", fontsize=10)

    plt.tight_layout()

    save_path = save_dir / "per_class_f1_comparison.png"
    fig.savefig(save_path, dpi=150, bbox_inches="tight")
    logger.info(f"Per-class F1 comparison saved: {save_path}")

    if show:
        plt.show()
    plt.close(fig)
